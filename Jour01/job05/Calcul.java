package Jour01.job05;

public class Calcul {
    public static void main(String[] args) {
        int nombre = 7; // Declare a variable named 'nombre' and assign it the value 7
        System.out.println(nombre); // Print the value of 'nombre'

        nombre += 3; // Add 3 to 'nombre'
        System.out.println(nombre); // Print the new value of 'nombre'

        nombre *= 5; // Multiply 'nombre' by 5
        System.out.println(nombre); // Print the new value of 'nombre'
    }
}

